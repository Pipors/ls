#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*bytes;
	size_t	i;

	bytes = (char *)s;
	i = 0;
	while (i < n)
	{
		bytes[i] = '\0';
		i++;
	}
}
